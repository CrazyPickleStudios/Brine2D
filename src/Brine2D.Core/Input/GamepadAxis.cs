namespace Brine2D.Core.Input;

/// <summary>
/// Gamepad axis identifiers.
/// </summary>
public enum GamepadAxis
{
    LeftX,          // Left stick X axis
    LeftY,          // Left stick Y axis
    RightX,         // Right stick X axis
    RightY,         // Right stick Y axis
    LeftTrigger,    // Left trigger (LT / L2)
    RightTrigger    // Right trigger (RT / R2)
}