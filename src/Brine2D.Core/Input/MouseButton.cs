namespace Brine2D.Core.Input;

/// <summary>
///     Defines the available mouse buttons that can be used for input.
/// </summary>
public enum MouseButton
{
    /// <summary>
    ///     The primary mouse button (typically the left button).
    /// </summary>
    Left,

    /// <summary>
    ///     The secondary mouse button (typically the right button).
    /// </summary>
    Right,

    /// <summary>
    ///     The middle mouse button (usually the scroll wheel button).
    /// </summary>
    Middle,

    /// <summary>
    ///     The first extended mouse button (often mapped to "Back").
    /// </summary>
    X1,

    /// <summary>
    ///     The second extended mouse button (often mapped to "Forward").
    /// </summary>
    X2
}