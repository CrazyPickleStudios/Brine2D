namespace Brine2D.Core.Input;

/// <summary>
/// Gamepad button codes (Xbox-style layout).
/// </summary>
public enum GamepadButton
{
    A,              // Xbox A / PlayStation Cross
    B,              // Xbox B / PlayStation Circle
    X,              // Xbox X / PlayStation Square
    Y,              // Xbox Y / PlayStation Triangle
    Back,           // Xbox Back / PlayStation Select
    Guide,          // Xbox Guide / PlayStation PS button
    Start,          // Xbox Start / PlayStation Start
    LeftStick,      // Left stick button
    RightStick,     // Right stick button
    LeftShoulder,   // LB / L1
    RightShoulder,  // RB / R1
    DPadUp,
    DPadDown,
    DPadLeft,
    DPadRight
}