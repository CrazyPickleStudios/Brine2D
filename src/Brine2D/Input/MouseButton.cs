namespace Brine2D.Input;

/// <summary>
///     Represents mouse buttons that can be used in input handling.
/// </summary>
public enum MouseButton
{
    /// <summary>
    ///     The primary (left) mouse button.
    /// </summary>
    Left,

    /// <summary>
    ///     The secondary (right) mouse button.
    /// </summary>
    Right,

    /// <summary>
    ///     The middle mouse button (typically the scroll wheel click).
    /// </summary>
    Middle,

    /// <summary>
    ///     The first extended mouse button (often a side button).
    /// </summary>
    X1,

    /// <summary>
    ///     The second extended mouse button (often a side button).
    /// </summary>
    X2
}